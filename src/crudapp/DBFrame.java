    public void display() {
        try {
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            dt.setRowCount(0);

            ps = con.prepareStatement("SELECT * FROM employee");
            rs = ps.executeQuery();

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getInt(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getDouble(4));

                dt.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

