    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String idValue = jTextField1.getText();
            String name = jTextField2.getText();
            String address = jTextField3.getText();
            String salaryValue = jTextField4.getText();

            if (idValue.equals("") || name.equals("") || address.equals("") || salaryValue.equals("")) {
                JOptionPane.showMessageDialog((rootPane), "Please fill all the values.");
                return;
            }

            int id = Integer.parseInt(idValue);
            double salary = Double.parseDouble(salaryValue);

            ps = con.prepareStatement("INSERT INTO employee VALUES (?, ?, ?, ?);");
            ps.setInt(1, id);
            ps.setString(2, name);
            ps.setString(3, address);
            ps.setDouble(4, salary);

            int row = ps.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(rootPane, "Employee Created Successfully!");
                clear();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Employee Creation Failed!");
            }
            display();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String idValue = jTextField1.getText();
            String name = jTextField2.getText();
            String address = jTextField3.getText();
            String salaryValue = jTextField4.getText();

            if (idValue.equals("")) {
                JOptionPane.showMessageDialog((rootPane), "Please fill in ID.");
                return;
            }
            
            if (name.equals("") || address.equals("") || salaryValue.equals("")) {
                JOptionPane.showMessageDialog((rootPane), "Please fill all the values.");
                return;
            }

            int id = Integer.parseInt(idValue);
            double salary = Double.parseDouble(salaryValue);

            ps = con.prepareStatement("UPDATE employee SET name=?, address=?, salary=? where id=?;");
            ps.setString(1, name);
            ps.setString(2, address);
            ps.setDouble(3, salary);
            ps.setInt(4, id);

            int row = ps.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(rootPane, "Employee Updated Successfully!");
                clear();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Employee Updation Failed!");
            }
            display();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

